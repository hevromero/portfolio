           <h1>Events &amp; Banners</h1>
          <h2>Attracting Visitors in the Office and at Events</h2>
          <p>During my time as a Graphic Design Specialist at Rambus, I was accountable for the majority of day-to-day design tasks for the Marketing team. This involved creating and updating product collateral, designing graphic assets for the corporate website and social media accounts, maintaining libraries of diagrams and icons, and designing for trade show events.</p>
          <p>I had the privilege of designing dozens of trade show booths from concept to completion. We used stock imagery in many of these, but also created custom assets as needed to convey a better narrative. The most notable example of this was for Mobile World Congress 2019, which needed a full wall of bespoke line art graphics to better showcase our technology. This process involved collaborating with Creative Design Manager and Events Coordinator to ensure that graphics are on brand and are correctly sized for each trade show booth.
          <p>I also designed supporting assets for trade show booths, such as badge holders, brochure ads, and even demo labels for groceries that were scannable with a Rambus mobile phone app.</p>
          <p>The Rambus brand and identity had changing needs as the company shifted its message. This required the occasional refresh of product posters for display in company meeting rooms and open spaces, and corporate banners that are displayed in Rambus offices and at events. I worked with the Creative Design Manager and Director of Marketing to find the best imagery to represent the various lines of products, adjust our taglines, and find ways to otherwise add visual interest.</p>
